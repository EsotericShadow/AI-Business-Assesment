import { NextRequest, NextResponse } from 'next/server'
import { XAIResponse } from '@/lib/xai-client'

export async function POST(request: NextRequest) {
  try {
    const { email, name, selectedProcesses, businessInfo } = await request.json()

    if (!email || !name || !selectedProcesses) {
      return NextResponse.json(
        { error: 'Missing required fields' },
        { status: 400 }
      )
    }

    // Generate detailed implementation plan using AI
    const implementationPlan = await generateImplementationPlan(
      selectedProcesses,
      businessInfo,
      name
    )

    // Store lead in database for follow-up
    await storeLead({
      email,
      name,
      businessInfo,
      selectedProcesses: selectedProcesses.map((p: any) => ({
        id: p.id,
        name: p.name,
        rating: p.userRating
      })),
      reportGenerated: true,
      timestamp: new Date()
    })

    // Send email with implementation plan
    await sendImplementationReport(email, name, implementationPlan)

    console.log('Report generated and sent to:', email)

    return NextResponse.json({
      success: true,
      message: 'Detailed implementation plan has been sent to your email',
      reportId: `report_${Date.now()}`
    })

  } catch (error) {
    console.error('Error generating report:', error)
    return NextResponse.json(
      { error: 'Failed to generate report' },
      { status: 500 }
    )
  }
}

